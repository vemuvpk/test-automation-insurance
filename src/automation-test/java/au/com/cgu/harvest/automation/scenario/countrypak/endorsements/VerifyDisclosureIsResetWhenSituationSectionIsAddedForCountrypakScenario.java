@RunWith( ParallelScenarioRunner.class )
@Scenario( "Verify Duty of Disclosure is Reset when New Section or Situation is added for Countrypak Scenario" )
public class VerifyDisclosureIsResetWhenSituationSectionIsAddedForCountrypakScenario extends
    AbstractScenario
{

    @Test
    public void execute()
    {
        WelcomePage welcomePage =
            performActivity( new LoginToExecutiveActivity() );

        PolicyDetailPage policyDetailPage =
            performActivity( new LaunchCountrypakActivity( welcomePage ) );

        policyDetailPage =
            performActivity( new PolicyDetailsActivity( policyDetailPage ) );

        CountrypakInsuranceHistoryPage insuranceHistoryPage =
            performActivity( new CountrypakInsuranceHistoryActivity( policyDetailPage ) );

        SituationDetailPage situationLevelSectionPage =
            performActivity( new CreateASituationForSituationLevelSectionsActivity(
                insuranceHistoryPage ) );

        DomesticBuildingAndContentsPage domesticBuildingAndContentsPage =
            performActivity( new AddDomesticBuildingActivity( situationLevelSectionPage ) );

        domesticBuildingAndContentsPage =
            performActivity( new AddDwellingType( domesticBuildingAndContentsPage ) );

        FarmPropertyPage farmPropertyPage =
            performActivity( new AddFarmPropertyActivity( domesticBuildingAndContentsPage ) );

        FinishPage finishPage =
            performActivity( new NavigateToFinishPageActivity( farmPropertyPage ) );

        NewBusinessPage newBusinessPage =
            performActivity( new FinishPolicyAsNewPolicyActivity(
                finishPage ) );
        newBusinessPage =
            performActivity( new GetAcceptanceActivity( newBusinessPage ) );

        policyDetailPage =
            performActivity( new ModifyAndModifyRiskDetailsActivity( newBusinessPage ) );

        policyDetailPage =
            performActivity( new AssertPolicyStageAndStatusInHeaderActivity( policyDetailPage,
                "Endorsement", "Complete", "Intermediary Statement" ) );
        TheftPage theftPage =
            performActivity( new AddTheftPageWithFarmContentsAndFarmMachinery( policyDetailPage ) );
        policyDetailPage = setDutyOfDisclosureAndEndorse( theftPage );

        domesticBuildingAndContentsPage =
            performActivity( new NavigateToDomesticBuildingPageActivity( policyDetailPage, 1 ) );

        domesticBuildingAndContentsPage =
            performActivity( new AddDwellingType( domesticBuildingAndContentsPage ) );
        policyDetailPage = setDutyOfDisclosureAndEndorse( theftPage );

        farmPropertyPage =
            performActivity( new NavigateToFarmPropertyPageActivity( policyDetailPage, 1 ) );
        farmPropertyPage =
            performActivity( new AddBuildingType( farmPropertyPage ) );
        policyDetailPage = setDutyOfDisclosureAndEndorse( theftPage );
    }

    private PolicyDetailPage setDutyOfDisclosureAndEndorse( TheftPage theftPage )
    {
        PolicyDetailPage policyDetailPage;
        FinishPage finishPage;
        NewBusinessPage newBusinessPage;
        policyDetailPage =
            performActivity( new NavigateToPolicyDetailPageActivity( theftPage ) );

        policyDetailPage =
            performActivity( new AssertAndSetDutyOfDisclosureActivity( policyDetailPage ) );

        finishPage =
            performActivity( new NavigateToFinishPageActivity( policyDetailPage ) );
        newBusinessPage =
            performActivity( new FinishPolicyForEndorsementActivity( finishPage ) );

        newBusinessPage =
            performActivity( new GetAcceptanceActivity( newBusinessPage ) );

        policyDetailPage =
            performActivity( new ModifyAndModifyRiskDetailsActivity( newBusinessPage ) );
        return policyDetailPage;
    }
}
